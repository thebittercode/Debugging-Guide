package com.bittercode;

//sum of squares of three numbers
public class ExampleC {

	public static void main(String[] args) {
		
		int a, b, c, res, sqa, sqb, sqc;
		
		a = 1;
		b = 2;
		c = 3;
		
		sqa = a * a;
		
		sqb = b * b;
		
		sqc = c * c;
		
		res = sqa + sqb + sqc;
		
		System.out.println("result: "+res);

	}

}
